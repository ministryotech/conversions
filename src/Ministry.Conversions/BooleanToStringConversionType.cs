namespace Ministry;

/// <summary>
/// Representation of a type of boolean and string conversion
/// </summary>
public enum BooleanToStringConversionType
{
    /// <summary>
    /// Represents the type of boolean and string conversion as Yes or No.
    /// </summary>
    YesOrNo,

    /// <summary>
    /// Represents the type of boolean and string conversion as True or False.
    /// </summary>
    TrueOrFalse,
    
    /// <summary>
    /// Represents a conversion type to convert a boolean value to a string with options Y or V.
    /// </summary>
    YOrN
}